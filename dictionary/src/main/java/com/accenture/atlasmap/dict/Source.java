package com.accenture.atlasmap.dict;

public class Source {
   String sourceHeader;
   SampleEnum sourceEnum;
   int sourceInt;
   Integer sourceNullableInt;
   
   public String getSourceHeader() {
      return this.sourceHeader;
   }

   public void setSourceHeader(String sourceHeader) {
      this.sourceHeader = sourceHeader;
   }

   public SampleEnum getSourceEnum() {
      return this.sourceEnum;
   }

   public void setSourceEnum(SampleEnum sourceEnum) {
      this.sourceEnum = sourceEnum;
   }

   public int getSourceInt() {
      return this.sourceInt;
   }

   public void setSourceInt(int sourceInt) {
      this.sourceInt = sourceInt;
   }

   public Integer getSourceNullableInt() {
      return this.sourceNullableInt;
   }

   public void setSourceNullableInt(Integer sourceNullableInt) {
      this.sourceNullableInt = sourceNullableInt;
   }
   
}
